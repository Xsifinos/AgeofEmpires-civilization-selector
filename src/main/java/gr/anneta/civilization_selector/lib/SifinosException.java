package gr.anneta.civilization_selector.lib;

/**
 *
 * @author chsifinos@gmail.com
 */
public class SifinosException extends Exception {

    /**
     * Constructs a {@code SifinosException} with no detail message.
     */
    public SifinosException() {
        super();
    }

    /**
     * Constructs a {@code SifinosException} with the specified
     * detail message.
     *
     * @param   s   the detail message.
     */
    public SifinosException(String s) {
        super(s);
    }
}
